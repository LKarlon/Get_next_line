#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	char	*save;

	save = s1;
	i = 0;
	while (*s1)
		s1++;
	while (s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (save);
}
